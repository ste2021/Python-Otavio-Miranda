# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número 
# positivo ou negativo. int sem sinal é positivo
# positivo

# float -> Número com ponto flutante - decimal = 1.0022.
# O tipo float representa qualquer número.
# positivo ou negativo com ponto flutuante.
# float sem sinal é considaderao positivo.

# A função type mostra o tipo que o Python interpreta.
# inferiu ao valor.
print(type('Estela'))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))